package oop;

public class Main {

    public static void main(String[] args) {
        //Decimal
        int x=15;
        System.out.println("The value of X is:  "+x);

        //Octel
        int y=0146;
        System.out.println("Decimal Of 146: "+y);

        //Hexa-Decimal
        int Z=0XFACE;
        System.out.println("Decimal Of FACE IS: "+Z);

        //Binary
        int a=0B1111;
        System.out.println("Decimal Of 1111: "+a);

        //Floating Literals
        float i=102.345f;
        double j=102.345;

        //Character Literals
        char b='a';
        char c=062;
        char d='\u0061';
        System.out.println("b: "+b);
        System.out.println("c: "+c);
        System.out.println("d: "+d);

        //String Literals
        String s= "Hello World";
        System.out.println(s);

        //Numeric Literals
        int x1=2_101_969;
        double x2= 1_29.09_19;
        System.out.println("x1: "+x1);
        System.out.println("x2: "+x2);

        //Escape Sequence
        System.out.print("\nNew Line");
        System.out.print("\nHello\tJava");
        System.out.print("\nHello\rJava");
        System.out.print("\n\"");
        System.out.print("\n\'");
        System.out.println("\n\\");
        boolean l=false;
        System.out.println(l);








    }
}
